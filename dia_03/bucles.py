# for loop (repetir algo una cantidad fija de veces)
for i in range(5):
    print("Hola", i)

print("------------")

# while loop (repetir algo mientras se cumpla una condición)
i = 0
while i < 5:
    print("Hola", i)
    i += 1

# Función range()
range(5)            # -> 0,1,2,3,4
range(1, 6)         # -> 1,2,3,4,5
range(1, 10, 2)     # -> 1,3,5,7,9